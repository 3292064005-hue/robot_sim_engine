@runtime_checkable
class MainWindowActionView(Protocol):
    """Narrow contract consumed by ``MainWindowActionMixin``.

    The goal is to keep action handlers independent from the full window shell so future
    presentation splits do not require the old monolithic ``MainWindowLike`` surface.
    """

    status_panel: object
    target_panel: object
    robot_panel: object
    playback_panel: object
    scene_controller: object
    plots_manager: object
    playback_render_scheduler: object
    robot_coordinator: object
    playback_task_coordinator: object
    scene_coordinator: object
    export_task_coordinator: object

    def _runtime_ops(self) -> RuntimeViewContract: ...
    def _robot_ops(self) -> RobotWorkflowContract: ...
    def _playback_ops(self) -> MotionWorkflowContract: ...
    def _run_presented(self, callback: Callable[[], object], *, title: str = '错误') -> object: ...
    def _run_status_projected(self, callback: Callable[[], object], *, prefix: str) -> object: ...
    def _set_playback_running(self, running: bool) -> None: ...
    def _project_exception(self, exc: Exception | str, *, title: str = '错误') -> None: ...
    def _playback_status_text(self) -> str: ...
    def project_playback_frame(self, frame: object, live: bool = False) -> None: ...
    def read_robot_import_request(self) -> dict[str, object] | None: ...
    def project_robot_imported(self, result: 'ImportedRobotResult') -> None: ...


