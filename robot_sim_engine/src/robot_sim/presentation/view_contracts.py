@runtime_checkable
class RuntimeViewContract(Protocol):
    """Minimal runtime projection/read contract required by coordinators."""

    @property
    def state(self) -> 'SessionState': ...

    @property
    def state_store(self) -> 'StateStore': ...

    @property
    def export_root(self) -> Path: ...

    @property
    def task_error_mapper(self) -> 'TaskErrorMapper': ...


@runtime_checkable
class RobotWorkflowContract(Protocol):
    def robot_entries(self) -> list['RobotCatalogEntry']: ...
    def importer_entries(self) -> list['ImporterDescriptor']: ...
    def load_robot(self, name: str) -> 'FKResult': ...
    def import_robot(self, source: str, importer_id: str | None = None) -> 'ImportedRobotResult': ...
    def save_current_robot(self, rows: object = None, home_q: object = None, name: str | None = None) -> Path: ...
    def run_fk(self, q: object = None) -> 'FKResult': ...


